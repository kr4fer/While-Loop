#include <iostream>

using namespace std;

int main()
{
    int a =1, b =10, c =2;

    cout << "a. ";
    while (a<=10) {
          cout <<a<<"\n";
          a++;
    }
    cout << "b. ";
    while (b>=1) {
        cout <<b<<"\n";
        b--;
    }
    cout << "c. ";
    while (c<=10) {
        cout <<c<<"\n";
        c++;
    }
    return 0;
}